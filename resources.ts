////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////
const dbResources: Resource[] = [];


////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////
export const getResource = (rsrcId?: string | null): (Resource | undefined) => {
    return rsrcId ? dbResources.find(r => r.rsrcId === rsrcId) : undefined;
}


